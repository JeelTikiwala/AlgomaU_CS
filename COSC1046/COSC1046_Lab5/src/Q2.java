
public class Q2 {

	public static void main(String[] args) {
		int rows = 5; //initialize rows

	    for (int i = 1; i <= rows; ++i) { //for loop for printing *
	      for (int j = 1; j <= i; ++j) {
	        System.out.print("* "); //To print the stars in sequence
	      }
	      System.out.println();
	    }

	}

}
